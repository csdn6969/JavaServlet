package entity;

/**
 *
 * @author Admin
 */
public class menu {
    private String name;
    public menu(){
        
    }
    public menu(String name){
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        StringBuilder sb = new StringBuilder();
        sb.append("menu{");
        sb.append("name=").append(name);
        sb.append('}');
        return sb.toString();
    }
    
}
