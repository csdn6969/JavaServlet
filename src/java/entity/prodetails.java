package entity;

/**
 *
 * @author Admin
 */
public class prodetails {
    private int id;
    private String name, img, price,brand;
      public prodetails(int id,String name, String img, String price, String brand){
        this.id = id;
        this.name = name;
        this.img = img;
        this.price = price;
        this.brand = brand;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    @Override
    public String toString() {
        return "prodetails{" + "id=" + id + ", name=" + name + ", img=" + img + ", price=" + price + ", brand=" + brand + '}';
    }
    
}
